package com.ospreydcs.dp.api.common;

import java.io.PrintStream;
import java.io.Serializable;

/**
 * <h1>
 * Data Provider unique identifier and Ingestion Service registration record.
 * </h1>
 * <p>
 * Data provider unique identifiers (UID) are created and managed by the Data Platform Ingestion Service.
 * Data providers must first register with the Ingestion Service using their unique name and optional
 * attributes.  Upon registration the Ingestion Service returns a UID for the provider which it must use
 * in all ingestion operations.  If a data provider has previously registered with the Ingestion Service its
 * original UID is returned.
 * </p>
 * <p>
 * Instances of <code>ProviderUID</code> are created by the <code>DpIngestionServiceImpl</code> client interface
 * at passed to client data providers upon registration.
 * </p>
 * 
 * @param   uid     the unique identifier for the Data Provider, as generated by the Ingestion Service
 * @param   name    the unique name of the Data Provider, as given by the provider to the Ingestion Service
 * @param   isNew   flag indicating whether or not the Ingestion Service has previously register Data Provider 
 *  
 * @author Christopher K. Allen
 * @since Mar 28, 2024
 *
 */
public final record ProviderUID(String uid, String name, boolean isNew) implements Serializable {
    
    
    //
    // Creators
    //
    
    /**
     * <p>
     * Creates a new <code>ProviderUID</code> instance from the given arguments.
     * </p>
     * 
     * @param   uid     the unique identifier for the Data Provider, as generated by the Ingestion Service
     * @param   name    the unique name of the Data Provider, as given by the provider to the Ingestion Service
     * @param   isNew   flag indicating whether or not the Ingestion Service has previously register Data Provider 
     * 
     * @return new <code>ProviderUID</code> record populated with the given arguments
     */
    public static ProviderUID   from (String uid, String name, boolean isNew) {
        return new ProviderUID(uid, name, isNew);
    }
    

    //
    // Operations
    //
    
    /**
     * <p>
     * Prints out a text description of the record fields to the given output stream.
     * </p>
     * <p>
     * A line-by-line text description of each record field is written to the given output.
     * The <code>strPad</code> is used to supply an optional whitespace character padding to the
     * left-hand side header for each line description.
     * </p>
     *   
     * @param ps        output stream to receive text description of record fields
     * @param strPad    white-space padding for each line header (or <code>null</code>)
     */
    public void printOut(PrintStream ps, String strPad) {
        if (strPad == null)
            strPad = "";
        
        ps.println(strPad + "Data Provider name : " + this.name);
        ps.println(strPad + "Data Provider UID  : " + this.uid);
        ps.println(strPad + "Is new provider    : " + this.isNew);
    }
        
}
